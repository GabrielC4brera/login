    private void textfielduserFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_textfielduserFocusLost
    String dato;
    dato = this.textfielduser.getText();
    if ("".equals(dato)){       //si no hay nada escrito
        textfielduser.setForeground(new java.awt.Color(163, 163, 163)); //pinta de gris el texto
        textfielduser.setText("Usuario"); //se vuelve a escribir usuario
        primerclickuser=0;      //se reinicia el primer click para automaticamente borrar en click
    }
    }//GEN-LAST:event_textfielduserFocusLost

    private void passwordfieldclaveFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_passwordfieldclaveFocusLost
    String pass = "";
    char [] password = passwordfieldclave.getPassword();
    for(int x=0; x<password.length; x++){
        pass+=password[x];
    }
    if ("".equals(pass)){       //si no hay nada escrito
        passwordfieldclave.setForeground(new java.awt.Color(163, 163, 163)); //pinta de gris el texto
        passwordfieldclave.setText("Contrase√±a"); //se vuelve a escribir usuario
        primerclickclave=0;      //se reinicia el primer click para automaticamente borrar en click
    }
    }//GEN-LAST:event_passwordfieldclaveFocusLost

