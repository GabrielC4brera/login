    private void BotonEntrarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_BotonEntrarMouseClicked
    LOGO.requestFocusInWindow();
    String valorPass = new String(passwordfieldclave.getPassword());
    LogueadoUsuario LogueadoUsuario=new LogueadoUsuario();
    LogueadoAdmin LogueadoAdmin = new LogueadoAdmin();
    if (textfielduser.getText().equals("admin") && valorPass.equals("admin")){
        System.out.println("Se ha logueado como administrador");
        LogueadoAdmin.setVisible(true);
        this.dispose();
    }else{

    if (textfielduser.getText().equalsIgnoreCase(usersave.getText()) && valorPass.equals(clavesave.getText())){ //comparar ignorando may√∫suclas en el nombre
        LogueadoUsuario.setVisible(true);
        System.out.println("Se ha logueado como usuario");
        this.dispose();
    }else{
    if (textfielduser.getText().equalsIgnoreCase("Usuario") || passwordfieldclave.getPassword().equals("Contrase√±a") || textfielduser.getText().equalsIgnoreCase("") || passwordfieldclave.getPassword().equals("")){
        new Error1(this, true).setVisible(true);///////////////////
        System.out.println("Debe llenar todos los campos");
    }else{  
        new Error9(this, true).setVisible(true);///////////////////
        System.out.println("El usuario y la clave no coinciden");
    }
    }
    }
    }//GEN-LAST:event_BotonEntrarMouseClicked

